CREATE TABLE IF NOT EXISTS users (
    user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name VARCHAR(255),
    email VARCHAR(255) UNIQUE NOT NULL,
    phone VARCHAR(20),
    password_hash VARCHAR(255),
    role user_role NOT NULL DEFAULT 'citizen',
    city VARCHAR(120),
    state VARCHAR(120),
    clerk_subject VARCHAR(255) UNIQUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS citizen_profiles (
    profile_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE REFERENCES users(user_id) ON DELETE CASCADE,
    age INT,
    gender VARCHAR(20),
    occupation VARCHAR(120),
    literacy_level VARCHAR(80)
);

CREATE TABLE IF NOT EXISTS admin_profiles (
    admin_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE REFERENCES users(user_id) ON DELETE CASCADE,
    department VARCHAR(120),
    designation VARCHAR(120),
    access_level INT NOT NULL DEFAULT 1
);

CREATE TABLE IF NOT EXISTS knowledge_base (
    kb_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(255) NOT NULL UNIQUE,
    attack_type VARCHAR(120) NOT NULL,
    description TEXT,
    precautions TEXT,
    safety_tips TEXT,
    severity_level severity_level NOT NULL DEFAULT 'medium',
    created_by BIGINT REFERENCES users(user_id) ON DELETE SET NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS attack_categories (
    category_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_name VARCHAR(120) UNIQUE NOT NULL,
    description TEXT
);

CREATE TABLE IF NOT EXISTS intelligence_reports (
    report_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    year INT NOT NULL,
    day DATE NOT NULL,
    amount_lost_inr NUMERIC(15,2) NOT NULL DEFAULT 0,
    incident_type VARCHAR(120) NOT NULL,
    city VARCHAR(120) NOT NULL,
    category VARCHAR(120) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_intelligence_report_row UNIQUE (year, day, incident_type, city, category)
);

CREATE TABLE IF NOT EXISTS incidents (
    incident_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
